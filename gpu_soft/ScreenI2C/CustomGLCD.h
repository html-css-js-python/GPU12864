void glcdWriteBus(uint8_t value)
{
  for (byte i = 0; i < 8; i++)
    digitalWrite(glcdDataPins[i], (value >> i) & 1);
}

void glcdPulse()
{
  digitalWrite(GLCD_EN, HIGH);
  delayMicroseconds(2);
  digitalWrite(GLCD_EN, LOW);
  delayMicroseconds(5);
}

void glcdSelect(bool left)
{
  digitalWrite(GLCD_CS1, left ? HIGH : LOW);
  digitalWrite(GLCD_CS2, left ? LOW : HIGH);
}

void glcdDeselect()
{
  digitalWrite(GLCD_CS1, LOW);
  digitalWrite(GLCD_CS2, LOW);
}

void glcdCommand(bool left, uint8_t cmd)
{
  glcdSelect(left);

  digitalWrite(GLCD_DI, LOW);
  glcdWriteBus(cmd);
  glcdPulse();

  glcdDeselect();
}

void glcdData(bool left, uint8_t data)
{
  glcdSelect(left);

  digitalWrite(GLCD_DI, HIGH);
  glcdWriteBus(data);
  glcdPulse();

  glcdDeselect();
}

void glcdSetPos(bool left, byte page, byte col)
{
  glcdCommand(left, 0xB8 | page);
  glcdCommand(left, 0x40 | col);
}

void glcdUpdate()
{
  for (bool left : {true, false})
  {
    for (byte page = 0; page < 8; page++)
    {
      glcdSetPos(left, page, 0);

      for (byte col = 0; col < 64; col++)
      {
        uint16_t index;

        if (left)
          index = page * 128 + col;
        else
          index = page * 128 + col + 64;

        glcdData(left, glcdBuffer[index]);
      }
    }
  }
}

void glcdClear()
{
  for (uint16_t i = 0; i < 1024; i++)
    glcdBuffer[i] = 0;

  glcdUpdate();
}

void glcdInit()
{
  for (byte i = 0; i < 8; i++)
    pinMode(glcdDataPins[i], OUTPUT);

  pinMode(GLCD_DI, OUTPUT);
  pinMode(GLCD_EN, OUTPUT);
  pinMode(GLCD_CS1, OUTPUT);
  pinMode(GLCD_CS2, OUTPUT);
  pinMode(GLCD_RES, OUTPUT);


  digitalWrite(GLCD_RES, LOW);
  delay(20);
  digitalWrite(GLCD_RES, HIGH);
  delay(20);


  glcdCommand(true, 0x3F);
  glcdCommand(false, 0x3F);

  delay(10);
}

void glcdDrawPixel(uint8_t x, uint8_t y, bool color = true)
{
  if (x >= 128 || y >= 64)
    return;


  uint16_t index = x + (y / 8) * 128;


  if (color)
    glcdBuffer[index] |= (1 << (y % 8));
  else
    glcdBuffer[index] &= ~(1 << (y % 8));
}

void glcdInvertPixel(uint8_t x, uint8_t y)
{
  if (x >= 128 || y >= 64)
    return;

  uint16_t index = x + (y / 8) * 128;

  glcdBuffer[index] ^= (1 << (y % 8));
}

void glcdDrawLine(int x0, int y0, int x1, int y1)
{
  int dx = abs(x1 - x0);
  int sx = x0 < x1 ? 1 : -1;

  int dy = -abs(y1 - y0);
  int sy = y0 < y1 ? 1 : -1;

  int err = dx + dy;

  while(true)
  {
    glcdDrawPixel(x0, y0);

    if(x0 == x1 && y0 == y1)
      break;

    int e2 = 2 * err;

    if(e2 >= dy)
    {
      err += dy;
      x0 += sx;
    }

    if(e2 <= dx)
    {
      err += dx;
      y0 += sy;
    }
  }
}

void glcdDrawLine(int x0, int y0, int x1, int y1, bool color = true)
{
  int dx = abs(x1 - x0);
  int sx = x0 < x1 ? 1 : -1;

  int dy = -abs(y1 - y0);
  int sy = y0 < y1 ? 1 : -1;

  int err = dx + dy;

  while(true)
  {
    glcdDrawPixel(x0, y0, color);

    if(x0 == x1 && y0 == y1)
      break;

    int e2 = 2 * err;

    if(e2 >= dy)
    {
      err += dy;
      x0 += sx;
    }

    if(e2 <= dx)
    {
      err += dx;
      y0 += sy;
    }
  }
}

void glcdDrawRect(int x, int y, int w, int h, bool color = true)
{
  glcdDrawLine(x, y, x + w - 1, y, color);
  glcdDrawLine(x, y, x, y + h - 1, color);
  glcdDrawLine(x + w - 1, y, x + w - 1, y + h - 1, color);
  glcdDrawLine(x, y + h - 1, x + w - 1, y + h - 1, color);
}

void glcdFillRect(int x, int y, int w, int h, bool color = true)
{
  for(int yy = y; yy < y + h; yy++)
  {
    glcdDrawLine(x, yy, x + w - 1, yy, color);
  }
}

void glcdDrawCircle(int cx, int cy, int r, bool color = true)
{
  int x = -r;
  int y = 0;
  int err = 2 - 2 * r;

  do
  {
    glcdDrawPixel(cx - x, cy + y, color);
    glcdDrawPixel(cx - y, cy - x, color);
    glcdDrawPixel(cx + x, cy - y, color);
    glcdDrawPixel(cx + y, cy + x, color);

    r = err;

    if(r <= y)
      err += ++y * 2 + 1;

    if(r > x || err > y)
      err += ++x * 2 + 1;

  } while(x < 0);
}

void glcdFillCircle(int cx, int cy, int r, bool color = true)
{
  for(int y = -r; y <= r; y++)
  {
    for(int x = -r; x <= r; x++)
    {
      if(x*x + y*y <= r*r)
        glcdDrawPixel(cx + x, cy + y, color);
    }
  }
}

void glcdDrawChar(uint8_t x, uint8_t y, char c, const GLCD_Font &font, bool color = true)
{
  if(c < font.first || c > font.last)
    return;

  uint16_t offset = (c - font.first) * font.size;

  for(uint8_t col = 0; col < font.width; col++)
  {
    uint8_t data = pgm_read_byte(&font.data[offset + col]);

    for(uint8_t row = 0; row < font.height; row++)
    {
      if(data & (1 << row))
        glcdDrawPixel(x + col, y + row, color);
    }
  }
}

void glcdDrawText(int x, int y, const char* text, const GLCD_Font &font, bool color = true)
{
  while(*text)
  {
    glcdDrawChar(x, y, *text, font, color);

    x += font.width + 1;
    text++;
  }
}

void glcdDrawBitmap(uint8_t x, uint8_t y, const uint8_t *bitmap, uint8_t width, uint8_t height)
{
  for(uint8_t yy = 0; yy < height; yy++)
  {
    for(uint8_t xx = 0; xx < width; xx++)
    {
      uint16_t index = xx + (yy / 8) * width;

      bool pixel = pgm_read_byte(&bitmap[index]) & (1 << (yy % 8));

      glcdDrawPixel(
        x + xx,
        y + yy,
        pixel
      );
    }
  }
}


